package PYX;
#------------------------------------------------------------------------------
# $Id: PYX.pm,v 1.3 2005-07-02 13:29:39 skim Exp $

# Version.
our $VERSION = 0.1;

# Modules.
use PYX::Utils;

#------------------------------------------------------------------------------
sub char {
#------------------------------------------------------------------------------
# Process char between tags.

	my $char = shift;
	return '-'.PYX::Utils::decode($char);
}

#------------------------------------------------------------------------------
sub start_tag {
#------------------------------------------------------------------------------
# Process begin tag.

	my ($tag, @attr) = @_;
	my @ret = ();
	push @ret, '('.$tag;
	while (@attr) {
		my ($key, $val) = (shift @attr, shift @attr);
		push @ret, "A$key ".PYX::Utils::decode($val);
	}
	return @ret;
}

#------------------------------------------------------------------------------
sub end_tag {
#------------------------------------------------------------------------------
# Process end tag.

	my $tag = shift;
	return ')'.$tag;
}

#------------------------------------------------------------------------------
sub instruction {
#------------------------------------------------------------------------------
# Process instruction.

	my ($target, $data) = @_;
	return '?'.$target.' '.PYX::Utils::decode($data);
}

#------------------------------------------------------------------------------
sub comment {
#------------------------------------------------------------------------------
# Process comment.

	my ($comment) = @_;
	return 'C'.PYX::Utils::decode($comment);
}

1;
